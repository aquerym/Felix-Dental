function Frame8() {
  const CARD_COUNT = 6;
  const DWELL = 120;       // px of scroll where a card sits alone before next one appears
  const TRANSITION = 300;  // px of scroll for the next card to slide in
  const SCROLL_PER_CARD = DWELL + TRANSITION;
  const STICKY_TOP = 20;
  const CARD_HEIGHT = 500; // increased for paragraph text under titles
  const HEADER_AREA = 130; // header height + bottom spacing

  const sectionRef = useRef<HTMLDivElement>(null);
  const cardRefs = useRef<(HTMLDivElement | null)[]>([]);
  const overlayRefs = useRef<(HTMLDivElement | null)[]>([]);

  // Animate cards based on how far the section has scrolled
  const animate = useCallback(() => {
    const section = sectionRef.current;
    if (!section) return;

    const sectionTop = section.getBoundingClientRect().top;
    const scrolled = Math.max(0, -(sectionTop - STICKY_TOP));

    for (let i = 0; i < CARD_COUNT; i++) {
      const card = cardRefs.current[i];
      const overlay = overlayRefs.current[i];
      if (!card) continue;

      // Card 0 is always present; others slide in after the previous card dwells
      let arrival = 1;
      if (i > 0) {
        const arriveStart = (i - 1) * SCROLL_PER_CARD + DWELL;
        arrival = Math.max(0, Math.min(1, (scrolled - arriveStart) / TRANSITION));
      }

      // How much the NEXT card covers this one (starts after dwell period)
      let coverage = 0;
      if (i < CARD_COUNT - 1) {
        const coverStart = i * SCROLL_PER_CARD + DWELL;
        coverage = Math.max(0, Math.min(1, (scrolled - coverStart) / TRANSITION));
      }

      if (arrival < 1) {
        // Card sliding in from below the viewport
        const slideY = (1 - arrival) * 200;
        card.style.transform = `translateY(${slideY}%) scale(${0.92 + arrival * 0.08})`;
        card.style.opacity = String(Math.max(0, arrival * 1.2));
      } else {
        // Card resting on screen, possibly being covered by next card
        const scale = 1 - coverage * 0.05;
        const yShift = -coverage * 12;
        card.style.transform = `translateY(${yShift}px) scale(${scale})`;
        card.style.opacity = "1";
      }

      if (overlay) overlay.style.opacity = String(coverage * 0.4);
    }
  }, []);

  useEffect(() => {
    const mql = window.matchMedia("(min-width: 768px)");
    if (mql.matches) return;

    let raf = 0;
    const onScroll = () => {
      cancelAnimationFrame(raf);
      raf = requestAnimationFrame(animate);
    };
    window.addEventListener("scroll", onScroll, { passive: true });
    window.addEventListener("resize", onScroll, { passive: true });
    onScroll();
    return () => {
      window.removeEventListener("scroll", onScroll);
      window.removeEventListener("resize", onScroll);
      cancelAnimationFrame(raf);
    };
  }, [animate]);

  const cards = [
    { node: <Frame4 />, key: "c1" },
    { node: <Frame7 />, key: "c2" },
    { node: <Frame10 />, key: "c3" },
    { node: <Frame14 />, key: "c4" },
    { node: <Frame17 />, key: "c5" },
    { node: <Frame20 />, key: "c6" },
  ];

  // Total scroll distance: all transitions + header + last card visible
  const totalHeight = (CARD_COUNT - 1) * SCROLL_PER_CARD + CARD_HEIGHT + HEADER_AREA;

  return (
    <div className="content-stretch flex flex-col items-start relative shrink-0 w-full">
      {/* Desktop — two-row grid layout */}
      <div className="hidden md:flex flex-col gap-[30px] items-start w-full">
        <Frame6 />
        <Frame13 />
      </div>

      {/* Mobile — scroll-driven stacking cards with fixed header */}
      <div
        ref={sectionRef}
        className="md:hidden relative w-full"
        style={{ height: `${totalHeight}px` }}
      >
        <div className="sticky w-full" style={{ top: `${STICKY_TOP}px` }}>
          {/* Section header stays fixed with the cards */}
          <div className="pb-[30px]">
            <Frame1 />
          </div>
          {/* Card stack area */}
          <div className="relative" style={{ height: `${CARD_HEIGHT}px` }}>
          {cards.map((card, i) => (
            <div
              key={card.key}
              ref={(el) => { cardRefs.current[i] = el; }}
              className="absolute inset-x-0 top-0 w-full will-change-transform"
              style={{
                zIndex: i + 1,
                transform: i === 0 ? "none" : "translateY(200%)",
                opacity: i === 0 ? 1 : 0,
              }}
            >
              {/* Card with shadow */}
              <div className="bg-white rounded-[24px] shadow-[0_2px_20px_rgba(79,94,123,0.12)] px-[8px] py-[20px] flex flex-col items-center relative overflow-hidden">
                {card.node}
                {/* Darkening overlay – opacity driven by scroll */}
                {i < CARD_COUNT - 1 && (
                  <div
                    ref={(el) => { overlayRefs.current[i] = el; }}
                    className="absolute inset-0 rounded-[24px] bg-[#4f5e7b] pointer-events-none"
                    style={{ opacity: 0 }}
                  />
                )}
              </div>
            </div>
          ))}
          </div>
        </div>
      </div>
    </div>
  );
}

